HARTREE_TO_J = nist.HARTREE2J
HARTREE_TO_KJMOL = HARTREE_TO_J*1e-3*nist.AVOGADRO
#BOHR_TO_NM = 0.52917721092e-1 # bohr in nm
BOHR_TO_NM = nist.BOHR * 1.0e-1
BOHR_TO_ANGSTROM = 10.0*BOHR_TO_NM
KJMOL_TO_HARTREE = 1.0/HARTREE_TO_KJMOL
NM_TO_BOHR = 1.0/BOHR_TO_NM
ANGSTROM_TO_BOHR = 1.0/BOHR_TO_ANGSTROM
DALTON_TO_ME = 1822.89
ME_TO_DALTON = 1.0/DALTON_TO_ME
#ATOMICTIME_TO_FS = 2.4188843265864e-2
ATOMICTIME_TO_FS = 1.0e15*nist.HBAR / nist.HARTREE2J
FS_TO_ATOMICTIME = 1.0/ATOMICTIME_TO_FS
EPSILON_0_OPENMM = 0.0005727657496161526
#HARTREE_TO_EV = 27.21138602
HARTREE_TO_EV = nist.HARTREE2EV
EV_TO_HARTREE = 1.0/HARTREE_TO_EV
KELVIN_TO_HARTREE = 0.000003166790852369942
HARTREE_TO_KELVIN = 1.0/KELVIN_TO_HARTREE